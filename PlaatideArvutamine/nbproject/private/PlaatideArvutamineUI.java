    private String setUhend1() {
        String uhend;
        int optionPoP = cmbPorandPikkus.getSelectedIndex();
        int optionPoL = cmbPorandLaius.getSelectedIndex();
        int optionPlP = cmbPlaatPikkus.getSelectedIndex();
        int optionPlL = cmbPlaatLaius.getSelectedIndex();
        if (optionPoP == 0) {
            uhend = " millimeetrit";
        } else if (optionPoP == 1) {
            uhend = " sentimeetrit";
        } else {
            uhend = " meetrit";
        }
        return uhend;
    }

